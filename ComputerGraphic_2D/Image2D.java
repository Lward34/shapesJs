    public BufferedImage generateImage(int[][] arr) {
        BufferedImage buffImg = new BufferedImage(minX_SIZE, minY_SIZE, BufferedImage.TYPE_INT_RGB);

        for (int x = 0; x < minX_SIZE; x++) {
            for (int y = 0; y < minY_SIZE; y++) {
                int pixel = arr[x][y];

                if (pixel == 0) {
                    pixel = Color.GREEN.getRGB();
                } else {
                    pixel = Color.BLUE.getRGB();
                }

                buffImg.setRGB(x, y, pixel);
            }
        }

        return buffImg;
    }

